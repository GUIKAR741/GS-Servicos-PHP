                        <p>
                            Cascading Style Sheets (CSS) é uma linguagem de folhas de estilo utilizada para definir a
                            apresentação de documentos escritos em uma linguagem de marcação, como HTML ou XML. O seu
                            principal benefício é a separação entre o formato e o conteúdo de um documento.
                        </p>
                        <p>
                            Em vez de colocar a formatação dentro do documento, o desenvolvedor cria um link (ligação)
                            para uma página que contém os estilos, procedendo de forma idêntica para todas as páginas
                            de um portal. Quando quiser alterar a aparência do portal basta portanto modificar apenas
                            um arquivo.
                        </p>
                        <p>
                            Com a variação de atualizações dos navegadores (browsers) como Internet Explorer que ficou
                            sem nova versão de 2001 a 2006, o suporte ao CSS pode variar. O Internet Explorer 6, por
                            exemplo, tem suporte total a CSS1 e praticamente nulo a CSS2. Navegadores mais modernos
                            como Google Chrome e Mozilla Firefox tem suporte maior, inclusive até a CSS3, ainda em
                            desenvolvimento.
                        </p>
                        <p>
                            A interpretação dos navegadores pode ser avaliada com o teste Acid2, que se tornou uma
                            forma base de revelar quão eficiente é o suporte de CSS, fazendo com que a nova versão em
                            desenvolvimento do Firefox seja totalmente compatível a ele assim como o Opera já é. O
                            Doctype informado ou a ausência dele determina o quirks mode ou o strict mode modificando
