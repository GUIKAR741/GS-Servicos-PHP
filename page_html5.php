                        <h1 class="cent pm0">HTML5</h1>
                        <p>
                            HTML5 (Hypertext Markup Language, versão 5) é uma linguagem para estruturação e apresentação
                            de conteúdo para a World Wide Web e é uma tecnologia chave da Internet originalmente
                            proposto por Opera Software. É a quinta versão da linguagem HTML. Esta nova versão traz
                            consigo importantes mudanças quanto ao papel do HTML no mundo da Web, através de novas
                            funcionalidades como semântica e acessibilidade. Possibilita o uso de novos recursos antes
                            possíveis apenas com a aplicação de outras tecnologias. Sua essência tem sido melhorar a
                            linguagem com o suporte para as mais recentes multimídias, enquanto a mantém facilmente
                            legível por seres humanos e consistentemente compreendida por computadores e outros
                            dispositivos (navegadores, parsers etc). O HTML5 será o novo padrão para HTML, XHTML, e
                            HTML DOM. Atualmente, está em fase de esboço, porém diversos navegadores já implementam
                            algumas de suas funcionalidades.
                        </p>
                        <p>
                            Após seus predecessores imediatos HTML 4.01 e XHTML 1.1, HTML5 é uma resposta à observação
                            de que o HTML e o XHTML, de uso comum na World Wide Web, é uma mistura de características
                            introduzidas por várias especificações, juntamente com aquelas introduzidas por software,
                            tais como os navegadores, aqueles estabelecidos pela prática comum, e os muitos erros de
                            sintaxe em documentos existentes na web. É, também, uma tentativa de definir uma única
                            linguagem simples de marcação que possa ser escrita em HTML ou em sintaxe XHTML. Isso
                            inclui modelos de processamento detalhados para incentivar implementações mais
                            interoperáveis; isso estende, melhora e racionaliza a marcação disponível para documentos,
                            e introduz marcações e interfaces de programação de aplicativos (APIs) para aplicações web
                            complexas. Pelas mesmas razões, HTML5 também é um candidato em potencial aplicações
                            multi-plataforma móveis. Muitos recursos do HTML5 tem sido construídos com a consideração
                            de ser capaz de executar em dispositivos de baixa potência como smartphones e tablets
                        </p>
